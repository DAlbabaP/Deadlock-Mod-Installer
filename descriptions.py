def get_mod_description(mod_name, lang='en'):
    """
    Получает описание мода на указанном языке.
    Если описание отсутствует, возвращает шаблон по умолчанию.
    """
    if mod_name not in MOD_DESCRIPTIONS:
        return {
            "title": "Unknown Mod",
            "description": "No description available",
            "author": "Unknown",
            "media": {
                "preview": None,
                "video": None
            }
        }
    
    mod = MOD_DESCRIPTIONS[mod_name]
    return {
        "title": mod["title"],
        "description": mod["descriptions"].get(lang, mod["descriptions"]["en"]),
        "author": mod["author"],
        "media": mod["media"]
    }
